print("Hello, it's simple calculator, print a first number")
a = int(input())
print("print second number")
b = int(input())
print("What you want to do? (*,//,+,-)")
c = input()
if c == "*":
    print(a * b)
if c == "//":
    print(a // b)
if c == "+":
    print(a + b)
if c == "-":
    print(a - b)








